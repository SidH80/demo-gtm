const IrsLogo = ({
    footer,
    logoUrl,
    logo,
    logoAlt,
    disableRef,
    logoLabel,
    logoLabelCurrent,
}) => {
    logoAlt = logoAlt ? logoAlt : 'IRS logo';
    const LogoComponent = logo ? (
        <img src={logo} alt={logoAlt} />
    ) : (
        defaultLogo(logoAlt)
    );

    if (footer) {
        return logoImg(logoAlt);
    }

    if (!logoUrl) {
        return (
            <div className='irs-logo__wrapper' data-testid='no-logo-url'>
                {logoImg(logoAlt)}
            </div>
        );
    }

    return (
        <div className='irs-logo__wrapper'>
            <NavLink
                to={logoUrl}
                aria-label={
                    window.location.pathname === logoUrl
                        ? logoLabelCurrent
                        : logoLabel
                }
                aria-current={null}
                onClick={
                    !disableRef
                        ? () => {
                              gaPageEventMethods.sendHeaderIrsLogoClick();
                          }
                        : e => {
                              e.preventDefault();
                          }
                }>
                {LogoComponent}
            </NavLink>
        </div>
    );
};

IrsLogo.propTypes = {
    /**@prop {boolean} footer - Determines if IRSLogo is being displayed  in footer or not */
    footer: PropTypes.bool,
    /**@prop {string} logoUrl - The url to link to for the IRS Logo */
    logoUrl: PropTypes.string,
    /**@prop {string} logo - image uri for IRS Logo, will display default svg if none is passed */
    logo: PropTypes.string,
    /**@prop {string} logoAlt - alt text for image */
    logoAlt: PropTypes.string,
    /**@prop {boolean} disableRef - disables the ref for the NavLink*/
    disableRef: PropTypes.bool,
    /**@prop {string} logoLabel - */
    logoLabel: PropTypes.string,
    /**@prop {string} logoLabelCurrent - */
    logoLabelCurrent: PropTypes.string,
};

IrsLogo.defaultProps = {
    footer: false,
    logoUrl: '',
    logo: '',
    logoAlt: '',
    disableRef: false,
    logoLabel: '',
    logoLabelCurrent: '',
};

export default IrsLogo;
